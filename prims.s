get_size_of_list:
    push rbp
    mov rbp, rsp
    mov rax, 0                          ;   counter
    mov rdx, [rbp + WORD_SIZE * 2]      ;   getArgument_to_check
    cmp byte [rdx], T_NIL
    je .finish
    .loop:
    cmp byte [rdx], T_PAIR
    jne .finish
    inc rax
    mov rdx, [rdx + 1 + WORD_SIZE]
    jmp .loop
    .finish:
    leave
    ret

get_n_arg_in_list:
    push rbp
    mov rbp, rsp
    mov r15, [rbp + WORD_SIZE * 2]
    mov rax, [rbp + WORD_SIZE * 3]
    .loop:
    cmp rax, 0
    je .finish
    dec rax
    mov r15, [r15 + WORD_SIZE + 1]
    jmp .loop
    .finish:
    mov rdx, [r15+1]
    leave
    ret

apply:
    push rbp
    mov rbp, rsp
    mov rdi, PARAM_COUNT
    mov r12, rdi
    cmp rdi, 2
    jl .wrong_type   ; at least two arguments
    .check1:
    mov rsi, PVAR(0)    
    cmp byte [rsi], T_CLOSURE       ; check procedure
    jne .wrong_type
    mov r15, rdi
    add r15, 3
    shl r15, 3
    add r15, rsp
    mov r15, qword [r15]
    cmp byte [r15], T_PAIR
    je .valid_input
    cmp byte [r15], T_NIL
    je .valid_input
    jmp .wrong_type
    .valid_input:
    push SOB_NIL_ADDRESS    ; MAGIC pushing
    dec rdi
    .pair_handler:
        push r15
        call get_size_of_list               ;rax = list size 
        pop r15
        mov r14, rax                        ;save list size
        .push_list_args_loop:
            cmp rax, 0
            je .push_rest_args
            dec rax
            push rax
            push r15
            call get_n_arg_in_list          ; return the n's variable at rdx
            pop r15
            pop rax     
            push rdx
            jmp .push_list_args_loop
        .push_rest_args:
            cmp rdi, 1
            je .push_args_num_lex_ret_rbp
            dec rdi
            push qword [rbp + (4 + rdi)*WORD_SIZE]
            jmp .push_rest_args
        .push_args_num_lex_ret_rbp:
            mov rax, PARAM_COUNT
            add rax, r14
            dec rax
            dec rax
            push rax
            push qword [rsi + 1]     ;push lexical of rsi
            push qword [rbp + WORD_SIZE]    ;push return address
            push qword [rbp]                ;push old_rbp
            mov rbx, rbp
            sub rbx, rsp                    ;num to fix
            shr rbx, 3
            inc rbx
        .shift_frame:
            mov rax, PARAM_COUNT
            add rax, 5
            mov rcx, 1
            .shift_loop:
                cmp rbx, rcx
                je .resolve_rsp_after_shifting
                dec rax
                mov r13, rbp
                shl rcx, 3
                sub r13, rcx
                shr rcx, 3
                mov r15, [r13]
                mov qword [rbp+WORD_SIZE*rax], r15
                inc rcx
                jmp .shift_loop
            .resolve_rsp_after_shifting:
                add r12, 5
                shl r12, 3
                add rsp, r12
            .ending:
                pop rbp
                jmp [rsi + 1 + WORD_SIZE]
    .wrong_type:
        mov rax, SOB_FALSE_ADDRESS
    .return:
    leave
    ret


